//to find the age with switch case 
let age=prompt("enter your age ")
switch (age){
    case "11":
        console.log("your age is 11")
        break
    case "12":
        console.log("your age is 13")
        break
    case "13":
        console.log("your age is 13")
        break
    case "14":
        console.log("your age is 14")
        break
    default:
        console.log("your age is not special")

}